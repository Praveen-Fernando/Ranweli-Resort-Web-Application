                  are displayed in each individual through body
                  types, or '<em>doshas</em>'. Maintaining the
                  balance of your '<em>dosha</em>' is the key to
                  successful Ayurvedic practice and this is done
                  through diet, exercise, massage and herbal
                  medicine.<br>
